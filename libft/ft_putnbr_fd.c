#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long int	nb;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	nb = n;
	if (nb < 0)
	{
		nb = nb * -1;
		ft_putchar_fd('-', fd);
	}
	if (nb >= 10)
		ft_putnbr_fd(nb / 10, fd);
	ft_putchar_fd((nb % 10) + 48, fd);
}
